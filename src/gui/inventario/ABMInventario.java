    private void nuevoGuardarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nuevoGuardarBtnActionPerformed
        // TODO add your handling code here:
        if(validarNuevo()){
            try {
                Equipos eq = new Equipos();
                eq.setEqDescripcion(nuevoNombreTxt.getText());
                eq.setModelo(nuevoModeloTxt.getText());
                if(!nuevoSerialTxt.getText().isEmpty())
                    eq.setEqNroserie(nuevoSerialTxt.getText());
                if(!nuevoObservacionesTxt.getText().isEmpty())
                    eq.setObservaciones(nuevoObservacionesTxt.getText());
                eq.setInvDepositos(((Depositos) nuevoDepositoCmb.getSelectedItem()).getCodigo());
                eq.setInvEquiposFamilias((nuevoFamiliaCmb.searchForeign()));
                eq.setEqActivo("S");
                eq.setCodigoBarras(generateBars());
                eq.setEqEstado("1");
                eq.setModelo(nuevoModeloTxt.getText());
                if(!nuevoAltoTxt.getText().isEmpty())
                    eq.setAlto(Integer.parseInt(nuevoAltoTxt.getText()));
                if(!nuevoAnchoTxt.getText().isEmpty())
                    eq.setAncho(Integer.parseInt(nuevoAnchoTxt.getText()));
                if(!nuevoLargoTxt.getText().isEmpty())
                    eq.setLargo(Integer.parseInt(nuevoLargoTxt.getText()));
                if(!nuevoPesoTxt.getText().isEmpty())
                    eq.setPeso(Integer.parseInt(nuevoPesoTxt.getText()));
                Equipos e = EquiposManager.instance().update(eq);
                JOptionPane.showMessageDialog(this, "Equipo guardado con exito!", "Guardado", JOptionPane.INFORMATION_MESSAGE);
                int res = JOptionPane.showConfirmDialog(this, "Desea imprimir la etiqueta con el codigo de barras?", "Imprimir", JOptionPane.YES_NO_OPTION );
                if(res == JOptionPane.YES_OPTION){
                    imprimirEtiqueta(e);
                }
                limpiarNuevo();
            } catch (RemoteException ex) {
                Logger.getLogger(ABMInventario.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error al grabar el equipo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_nuevoGuardarBtnActionPerformed

