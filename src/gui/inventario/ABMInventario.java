    private String agregarCeros(String string, int largo) {
        String ceros = "";

        int cantidad = largo - string.length();

        if (cantidad >= 1) {
            for (int i = 0; i < cantidad; i++) {
                ceros += "0";
            }

            return (ceros + string);
        } else {
            return string;
        }
    }
    
    private void imprimirEtiqueta(Equipos eq){
        if (eq != null) {
            try {
                PrintService printService = PrintServiceLookup.lookupDefaultPrintService();
                
                String zplCommand2 = "^XA\n"
                        + "^FO20, 50^ADN, 11, 2^FD" + eq.getEqDescripcion() + " - " + eq.getModelo() + "^FS\n"
                        + "^FO65, 80^BY3\n"
                        + "^B3N,N,100,Y,N\n"
                        + "^FD" + agregarCeros(eq.getCodigoBarras(), 6) + "^FS\n"
                        + "^XZ";
                // convertimos el comando a bytes
                byte[] by = zplCommand2.getBytes();
                DocFlavor flavor = DocFlavor.BYTE_ARRAY.AUTOSENSE;
                Doc doc = new SimpleDoc(by, flavor, null);

                // creamos el printjob
                DocPrintJob job = printService.createPrintJob();

                // imprimimos  
                job.print(doc, null);
            } catch (PrintException ex) {
                Logger.getLogger(ABMInventario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
