    private void refreshTable(){
        try {            
            ((BusquedaEquiposTableModel)jTable1.getModel()).clear();
            Equipos[] equiposEncontrados = null;
            if(modMarcaRad.isSelected())
                equiposEncontrados = EquiposManager.instance().findByField("eqDescripcion", modNombreTxt.getText());
            else if(modFamiliaRad.isSelected())
                equiposEncontrados = EquiposManager.instance().findByField("invEquiposFamilias", modFamiliaCmb.searchForeign());
            else if (modCodBarraRad.isSelected())
                equiposEncontrados = EquiposManager.instance().findByField("codigoBarras", modCodigoTxt.getText());
            else if (modDepositoRad.isSelected())
                equiposEncontrados = EquiposManager.instance().findByField("invDepositos", String.valueOf(modDepositoCmb.getSelectedIndex()));
            else if (modNroSerieRad.isSelected())
                equiposEncontrados = EquiposManager.instance().findByField("eqNroserie", modSerialTxt.getText());
            
            if (equiposEncontrados.length > 0 || equiposEncontrados == null) {
                BusquedaEquiposTableModel model = (BusquedaEquiposTableModel) jTable1.getModel();

                for (Equipos eq : equiposEncontrados) {
                    model.addRow(eq);
                }
                jTable1.setModel(model);
                jTable1.updateUI();
            }
        } catch (RemoteException ex) {
            Logger.getLogger(ABMInventario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private boolean validarNuevo(){
        boolean result = true;
        jLabel2.setForeground(Color.black);
        jLabel3.setForeground(Color.black);
        jLabel17.setForeground(Color.black);
        jLabel12.setForeground(Color.black);
        if(nuevoNombreTxt.getText().isEmpty()){
            jLabel2.setForeground(Color.red);
            result=false;
        }

        if(nuevoFamiliaCmb.getSelectedIndex()==0){
            jLabel3.setForeground(Color.red);
            result=false;
        }
        if(nuevoDepositoCmb.getSelectedIndex()==0){
            jLabel17.setForeground(Color.red);
            result=false;
        }
        if(nuevoModeloTxt.getText().isEmpty()){
            jLabel12.setForeground(Color.red);
            result=false;
        }
        return result;
    }

    private void cargarItemsComboBoxfamilias()
    {
        try {
        	modFamiliaCmb.loadItems();
            nuevoFamiliaCmb.loadItems();
            modDepositoCmb.addItem("Seleccione un deposito");
            nuevoDepositoCmb.addItem("Seleccione un deposito");
            Depositos[] depMod = DepositosManager.instance().getAll();
            if (depMod != null) {
                for (Depositos eq : depMod) {
                    modDepositoCmb.addItem(eq);
                    nuevoDepositoCmb.addItem(eq);
                }
            }

        } catch (RemoteException ex) {
            Logger.getLogger(ABMInventario.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error al cargar datos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

