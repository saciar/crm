    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ControlEquiposTableModel model = (ControlEquiposTableModel) jTable1.getModel();
        ControlEquiposItem itemSeleccionado = model.getRow(jTable1.getSelectedRow());
        if (itemSeleccionado != null) {
            model.removeRow(jTable1.getSelectedRow());
            jTable1.setModel(model);
            jTable1.updateUI();
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un item de la grilla", "Error", JOptionPane.ERROR_MESSAGE);
        }
}//GEN-LAST:event_jButton2ActionPerformed

    private void jTextField2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                Equipos[] equiposEncontrados;
                equiposEncontrados = EquiposManager.instance().findByFieldExactly("codigoBarras", jTextField2.getText());

                if (equiposEncontrados.length > 0) {
                    ControlEquiposTableModel model = (ControlEquiposTableModel) jTable1.getModel();

                    for (Equipos eq : equiposEncontrados) {
                        ControlEquiposItem item = new ControlEquiposItem();
                        item.setCodigoBarras(eq.getCodigoBarras());
                        item.setNoSerie(eq.getEqNroserie());
                        item.setNombre(eq.getEqDescripcion());
                        item.setCodigoEquipo(eq.getCodigo());
                        model.addRow(item);
                    }
                    jTable1.setModel(model);
                    jTable1.updateUI();
                }
            } catch (RemoteException ex) {
                Logger.getLogger(ABMInventario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
}//GEN-LAST:event_jTextField2KeyPressed

    
