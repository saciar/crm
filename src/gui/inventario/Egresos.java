    private boolean tieneOS(String os){
        try {
            if(os.equals("x")){
                return true;
            }
            Object[] estado = PresupuestosManager.instance().buscarEstadoActual(Long.parseLong(os));
            for (int j = 0; j < estado.length; j++) {
                Object[] estados = (Object[]) estado[j];
                if ((Integer) estados[5] == 1) {
                    return true;
                } else {
                    return false;
                }
            }
            return false;
        } catch (RemoteException ex) {
            Logger.getLogger(Egresos.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }
    }

    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        try {
            ControlEquiposTableModel model = (ControlEquiposTableModel) jTable1.getModel();
            EgresoEquipoManager manager = EgresoEquipoManager.instance();
            EquiposManager equipoManager = EquiposManager.instance();
            if (tieneOS(jTextField1.getText())) {
                EgresoEquipo[] eg = manager.findByField("nroOrden", jTextField1.getText());
                for (EgresoEquipo egreso : eg) {
                    Equipos[] equipo = equipoManager.findByFieldExactly("codigo", egreso.getCodEquipo());
                    ControlEquiposItem item = new ControlEquiposItem();
                    item.setCodigoEquipo(equipo[0].getCodigo());
                    item.setCodigoBarras(equipo[0].getCodigoBarras());
                    item.setNoSerie(equipo[0].getEqNroserie());
                    item.setNombre(equipo[0].getEqDescripcion());
                    item.setIdEgreso(egreso.getCodigo());
                    model.addRow(item);
                    jTable1.setModel(model);
                    jTable1.updateUI();
                }
                jPanel1.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No existe orden de servicio con ese nro.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (RemoteException ex) {
            Logger.getLogger(Egresos.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (JOptionPane.showOptionDialog(this, "Esta seguro que quiere grabar los cambios?", "Guardar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, rootPane) == 0) {
            try {
                ControlEquiposTableModel model = (ControlEquiposTableModel) jTable1.getModel();
                EgresoEquipoManager manager = EgresoEquipoManager.instance();
                for (ControlEquiposItem eq : model.getRows()) {
                    EgresoEquipo eg = new EgresoEquipo();
                    if(eq.getCodigoEquipo()!=null)
                        eg.setCodigo(eq.getIdEgreso());
                    eg.setCodEquipo(eq.getCodigoEquipo());
                    //eg.setNroOrden("1");
                    manager.update(eg);
                }
            } catch (RemoteException ex) {
                Logger.getLogger(Egresos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
}//GEN-LAST:event_jButton6ActionPerformed

